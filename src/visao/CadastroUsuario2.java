    private void jtUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtUsuarioMouseClicked
       String usu_nome = ""+jtUsuario.getValueAt (jtUsuario.getSelectedRow(), 1);
       conex.conexao();
       conex.executaSql ("select * from usuarios where usu_nome='"+usu_nome+"'");
       try{
           conex.rs.first();
           jtCod.setText(String.valueOf(conex.rs.getInt("usu_cod")));
           jtNome.setText(conex.rs.getString("usu_nome"));
           jtLogin.setText(conex.rs.getString("usu_usu"));
       }catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Erro ao selecionar dados"+ex);
       }
       conex.desconecta();
    
       
    }//GEN-LAST:event_jtUsuarioMouseClicked

    public  void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID","Nome Usuario"};
        conex.conexao();
        conex.executaSql(Sql);
        try {
            conex.rs.first(); 
            do{
 dados.add(new Object[]{conex.rs.getInt("usu_cod"),conex.rs.getString("usu_nome")});
            }while(conex.rs.next()); 
            
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane,"Erro ao preencher ArrayList"+ex);
                }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
       
        jtUsuario.setModel(modelo);
        jtUsuario.getColumnModel().getColumn(0).setPreferredWidth(23);
        jtUsuario.getColumnModel().getColumn(0).setResizable(false);
        jtUsuario.getColumnModel().getColumn(1).setPreferredWidth(180);
        jtUsuario.getColumnModel().getColumn(1).setResizable(false);
     
        jtUsuario.getTableHeader().setReorderingAllowed(false);
        jtUsuario.setAutoResizeMode(jtUsuario.AUTO_RESIZE_OFF);
        jtUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
        
        
    }
    
        
  

