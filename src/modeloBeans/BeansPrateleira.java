package modeloBeans;


public class BeansPrateleira {
    private Integer pratCod;
    private String pratNome;
    private String pratRua;
    private String pratColuna;
    private String pratLinha;

    public Integer getPratCod() {
        return pratCod;
    }

    public void setPratCod(Integer pratCod) {
        this.pratCod = pratCod;
    }

    public String getPratNome() {
        return pratNome;
    }

    public void setPratNome(String pratNome) {
        this.pratNome = pratNome;
    }

    public String getPratRua() {
        return pratRua;
    }

    public void setPratRua(String pratRua) {
        this.pratRua = pratRua;
    }

    public String getPratColuna() {
        return pratColuna;
    }

    public void setPratColuna(String pratColuna) {
        this.pratColuna = pratColuna;
    }

    public String getPratLinha() {
        return pratLinha;
    }

    public void setPratLinha(String pratLinha) {
        this.pratLinha = pratLinha;
    }
    
    
    
}
